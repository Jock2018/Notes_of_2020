/*
    时间：2020年2月20日09:48:37
    功能：
        for循环求1-100之间所有奇数之和
    总结：
        1. for 的默认作用范围是一个语句。
        2. for 作用于多个语句，需要使用 {} 把多个语句括起来
*/

# include <stdio.h>

int main(void)
{
    int i;
    int sum = 0;

    for (i=1; i<100; i+=2)  // i+=2 等价于 i = i + 2
    {
        sum += i;  // 等价于 sum = sum + i;
    }
    
    printf("i = %d, sum = %d\n", i, sum);
    
    return 0;
}

/*
    在VSCode中的输出结果是：
--------------------------
i = 101, sum = 2500
--------------------------
*/