def main():

    print("被除数和除数均为整数时：") 
    print("11/4 =", 11/4)
    print("11//4 =", 11//4)
    print("11%4 =", 11%4)

    print("被除数为负数时：") 

    print("(-11)/4 =", (-11)/4)
    print("(-11)//4 =", (-11)//4)
    print("(-11)%4 =", (-11)%4)

    print("被除数为小数时：") 

    print("(-11.7)/4 =", (-11.7)/4)
    print("(-11.7)//4 =", (-11.7)//4)
    print("(-11.7)%4 =", (-11.7)%4)  # -11.7 + 4*(-11//4)) = -11.7 + 12
    print(-11.7 + 12)  # 这里计算机没法准确计算 0.3 

    print("除数为小数时：") 
    print("(-11)/3.5 =", (-11)/3.5)
    print("(-11)//3.5 =", (-11)//3.5)
    print("(-11)%3.5 =", (-11)%3.5)  # -11 - 3.5*(-11//3.5) = -11 + 3.5*4
    print(-11 + 3.5*4)

    return None

if __name__ == '__main__':
    main()

